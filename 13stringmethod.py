a="dax!!!!!!dax"
print(len(a))
print(a.upper())
print(a.lower())
print(a.rstrip("!")) #removes ! from right side
print(a.replace("dax","dhanani"))
print(a.split(" ")) #gives element in the list
print(a.capitalize()) #capitalize the first letter
print(a.center(50)) #50 spaces in the center    
print(a.count("dax")) #counts the number of dax
print(a.endswith("dax")) #true or false
print(a.endswith("!",3,7)) 
print(a.find("!")) #find the index of !
print(a.find("z")) #returns -1 if not found
print(a.index("dax!!!")) #returns error if not found gives index of dax
print(a.isalnum()) #true or false if all are alphanumeric or not
print(a.isalpha()) #true or false if all are alphabets or not
print(a.isupper()) 
print(a.islower())
print(a.isprintable()) #true or false if all are printable or not \n \t etc are not printable
print(a.isspace()) #true or false if all are spaces or not
print(a.istitle()) #true or false if all are titlecase or not
print(a.startswith("dax")) #true or false if starts with dax or not
print(a.swapcase()) #swap upper to lower and lower to upper
print(a.title()) #Make first letter of each word is capital











